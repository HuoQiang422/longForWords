package com.hanli.longforwords01.etymon;

public class EtymonWord {
    private String etymon;
    private String exp;
    private String id;

    public String getEtymon() {
        return etymon;
    }

    public void setEtymon(String etymon) {
        this.etymon = etymon;
    }

    public String getExp() {
        return exp;
    }

    public void setExp(String exp) {
        this.exp = exp;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
